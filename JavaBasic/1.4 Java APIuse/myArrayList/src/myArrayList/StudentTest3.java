package myArrayList;

public class StudentTest3 {
	private String name;
	private int age;
	
	
	public StudentTest3() {
		
	}


	public StudentTest3(String name, int age) {
		this.name = name;
		this.age = age;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}
	
	
	
}
