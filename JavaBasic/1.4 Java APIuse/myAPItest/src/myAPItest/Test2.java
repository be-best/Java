package myAPItest;

import java.util.Scanner;

/*
 * 分析以下需求,并用代码实现
 * 1.键盘录入一个源字符串由字符串变量scrStr接收
 * 2.键盘录入一个要删除的字符串由字符串变量delStr接收
 * 3.要求:删除该字scrStr符串中的所有delStr字符串（最终的字符串中不能包含delStr），要求打印删除后的结果以及删除了几个delStr字符串
 * 4.代码运行打印格式:
 * 请输入源字符串:java woaijava,i like jajavava i enjoy java
 * 请输入要删除的字符串:java
 * 控制台输出结果:源字符串中总共包含:5 个 java 删除java后的字符串为: woai,i like  i enjoy
 */

public class Test2 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		//输入第一个字符串
		System.out.println("请输入第一个字符串：");
		String str1 = sc.nextLine();
		//输入要删除的字符串
		System.out.println("请输入要删除的字符串：");
		String str2 = sc.nextLine();
		
		int count = 0;
		//indexOf返回指定字符串第一次出现的索引
		while((str1.indexOf(str2)) != -1) {
			//将指定字符串str2删掉
			str1 = str1.replaceFirst(str2, "");
			//次数
			count++;
		}
		System.out.println("源字符串共包含"+count+"个"+str2+",删除"+str2+"后的字符串为："+str1);
	}

	
}
